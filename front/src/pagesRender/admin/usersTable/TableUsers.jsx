import React from "react"
import TableUsersHeader from "./TableUsersHeader"
import TableUsersRow from "./TableUsersRow"
import "./tableUsers.scss"

// ■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■■

export default function TableUsers({ users, setUsers, filters, setFilters, mode }) {
	const handleLike = (userId, val) => {
		const updatedUsers = users.map((user) => (user.id === userId ? { ...user, is_liked_by_me: val } : user))
		setUsers(updatedUsers)
	}

	return (
		<table className="TableUsers table table-xs table-striped table-bordered  table-hover">
			<thead>
				<TableUsersHeader filters={filters} setFilters={setFilters} />
			</thead>
			<tbody>
				{users &&
					users.map((user) => (
						<TableUsersRow
							key={"user-table-" + user.id}
							user={user}
							filters={filters}
							handleLike={handleLike}
						/>
					))}
			</tbody>
		</table>
	)
}
